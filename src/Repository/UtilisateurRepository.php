    }
    /**
     * this fnct add/update utilisateur in DB
     * @param Utilisateur $utilisateur
     * @param bool $flush
     * @return Utilisateur
     */
    public function save(Utilisateur $utilisateur , bool $flush = false): Utilisateur {
        $em = $this->getEntityManager();
        $em->persist($utilisateur);

        if($flush == true) {
            $em->flush();
        }
        return $utilisateur;
    }

